    private void sarki_ekleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sarki_ekleActionPerformed
        mesaj_yazisi.setText("");

        String sarkiAdi = sarki_adi.getText();
        String sanatciId = sanatci_id.getText();
        String albumId = album_id.getText();
        String tur = sarki_turu_id.getText();
        String sure = sarki_suresi.getText();
        String dinlenme = dinlenme_sayisi.getText();
        String tarih = sarki_tarih.getText();
        String info = bilgi.getText();

        //calÄ±san ekleme buradan
        islemler.sarkiEkle(sarkiAdi, Integer.valueOf(sanatciId), Integer.valueOf(albumId), tur, sure, dinlenme, tarih, info);

        sarkilariGoruntule();
        mesaj_yazisi.setText("Yeni sarki basariyla eklendi");

    }//GEN-LAST:event_sarki_ekleActionPerformed

    private void sarki_guncelleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sarki_guncelleActionPerformed

        String sarkiAdi = sarki_adi.getText();
        String sanatciId = sanatci_id.getText();
        String albumId = album_id.getText();
        String tur = sarki_turu_id.getText();
        String sure = sarki_suresi.getText();
        String dinlenme = dinlenme_sayisi.getText();
        String tarih = sarki_tarih.getText();
        String info = bilgi.getText();

        int selected_row = sarki_tablosu.getSelectedRow();

        if (selected_row == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("Tablo suan da bos");
            } else {
                mesaj_yazisi.setText("Lutfen tablodan guncellenek sarkiyi secin");
            }
        } else {
            int id = (int) model.getValueAt(selected_row, 0);

            islemler.sarkiGuncelle(id, sarkiAdi, Integer.valueOf(sanatciId), Integer.valueOf(albumId), tur, sure, dinlenme, tarih, info);
            sarkilariGoruntule();
            mesaj_yazisi.setText("Sarki guncellenmistir");

        }

    }//GEN-LAST:event_sarki_guncelleActionPerformed

    private void sarki_silActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sarki_silActionPerformed
        mesaj_yazisi.setText("");

        int selectedrow = sarki_tablosu.getSelectedRow();

        if (selectedrow == -1) {
            if (model.getRowCount() == 0) {
                mesaj_yazisi.setText("Sarki tablosu suan bos");
            } else {
                mesaj_yazisi.setText("Silicenek sarki secin");
            }
        } else {
            int id = (int) model.getValueAt(selectedrow, 0);

            islemler.sarkiSil(id);
            sarkilariGoruntule();
            mesaj_yazisi.setText("Sarki silinmistir");
        }

    }//GEN-LAST:event_sarki_silActionPerformed

